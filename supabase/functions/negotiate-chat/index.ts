import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers": "authorization, x-client-info, apikey, content-type, x-supabase-client-platform, x-supabase-client-platform-version, x-supabase-client-runtime, x-supabase-client-runtime-version",
};

serve(async (req) => {
  if (req.method === "OPTIONS") return new Response(null, { headers: corsHeaders });

  try {
    const { messages, contractContext } = await req.json();

    if (!messages || !Array.isArray(messages)) {
      return new Response(JSON.stringify({ error: "messages array is required" }), {
        status: 400,
        headers: { ...corsHeaders, "Content-Type": "application/json" },
      });
    }

    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    if (!LOVABLE_API_KEY) throw new Error("LOVABLE_API_KEY is not configured");

    const systemPrompt = contractContext
      ? `You are an expert healthcare contract negotiation analyst. You are analyzing a contract with ${contractContext.provider}.

Contract details:
- Provider: ${contractContext.provider}
- Last Modified: ${contractContext.lastModified}
- Risk Level: ${contractContext.riskLevel}
- Open Risks: ${contractContext.openRisks}
- Potential Savings: ${contractContext.potentialSavings}

You specialize in:
- Identifying high-risk clauses (escalation, termination, liability, indemnity)
- Benchmarking rates against market data for healthcare providers
- Suggesting counter-proposals backed by data
- Financial impact analysis of contract terms
- Compliance with healthcare regulations

Always provide structured, actionable insights. Use markdown formatting with headers, bullet points, and bold text for key figures. When discussing financials, be specific with numbers. Reference market benchmarks and comparable contracts when possible.`
      : `You are an expert healthcare contract negotiation advisor. You help with:
- General negotiation strategies and best practices
- Industry benchmarks for healthcare contracts
- Price negotiation tactics
- Escalation handling strategies
- Contract clause analysis

Provide clear, actionable advice using markdown formatting. Be specific with examples and data when possible.`;

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-3-flash-preview",
        messages: [
          { role: "system", content: systemPrompt },
          ...messages,
        ],
        stream: true,
      }),
    });

    if (!response.ok) {
      if (response.status === 429) {
        return new Response(JSON.stringify({ error: "Rate limited. Please try again in a moment." }), {
          status: 429,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      if (response.status === 402) {
        return new Response(JSON.stringify({ error: "AI credits exhausted. Please add funds in Settings > Workspace > Usage." }), {
          status: 402,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      const t = await response.text();
      console.error("AI gateway error:", response.status, t);
      return new Response(JSON.stringify({ error: "AI service error" }), {
        status: 500,
        headers: { ...corsHeaders, "Content-Type": "application/json" },
      });
    }

    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (e) {
    console.error("negotiate-chat error:", e);
    return new Response(JSON.stringify({ error: e instanceof Error ? e.message : "Unknown error" }), {
      status: 500,
      headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  }
});
